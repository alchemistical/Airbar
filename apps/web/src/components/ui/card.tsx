import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "elevated" | "interactive" | "premium";
  }
>(({ className, variant = "default", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground transition-all duration-300",
      {
        "shadow-sm hover:shadow-md border-gray-200": variant === "default",
        "shadow-lg shadow-gray-200/50 border-gray-100": variant === "elevated",
        "shadow-md hover:shadow-xl hover:-translate-y-1 cursor-pointer border-gray-200":
          variant === "interactive",
        "shadow-xl shadow-purple-200/50 border-purple-100 bg-gradient-to-br from-white via-purple-50/30 to-blue-50/30":
          variant === "premium",
      },
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

